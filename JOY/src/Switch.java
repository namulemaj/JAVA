//Java Switch Example where we are omitting the
//break statement
public class Switch {
    public static void main(String[] args) {
        int number=200;
        //switch expression with int value
        switch(number){
            //switch cases without break statements
            case 10: System.out.println("110");
            case 20: System.out.println("120");
            case 30: System.out.println("130");
            default:System.out.println("Not in 110, 120 or 130");
        }
    }
}



